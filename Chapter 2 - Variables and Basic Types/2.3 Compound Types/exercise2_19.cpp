// Explain the key differences between pointers and references.

// ANSWER:

// A reference is an alias for another variable, it is not an object.
// A reference must be initialized at declaration. 
// A reference's value cannot be changed.

// A pointer is an object which holds the memory of another object.
// It doesn't have to be initialized at declaration, but it is good practice to do so.
// Pointer's value can be changed.